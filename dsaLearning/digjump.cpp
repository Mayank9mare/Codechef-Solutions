int bfs(vector<int> v[],int n,string s){
    int vis[n]={0};
    int k=0;
    queue<pll> q;
    q.push({0,0});
    while(q.size()){
       
        pll p=q.front();
        q.pop();
        if(p.first==(n-1))return p.second;
        
        if(vis[p.first]==1)continue;
        vis[p.first]=1;
        int val=s[p.first]-'0';
        for(int i=0;i<v[val].size();i++){
            
            if(vis[v[val][i]]==1)continue;
            q.push({v[val][i],p.second+1});
            }
        v[val].clear();
        if((p.first-1)>=0 && vis[p.first-1]!=1){
            q.push({p.first-1,p.second+1});
        }
        if((p.first+1)<n && vis[p.first+1]!=1){
            q.push({p.first+1,p.second+1});
        }
        
       


    }
    return -1;


}


