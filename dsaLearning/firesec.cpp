ll power(ll x,int y){
    ll p=mod;
    ll res =1;
    x=x%p;
    while(y>0){
        if(y&1){
            res=(res*x)%p;
        }
            y=y>>1;
            x=(x*x)%p;
        }
        return res;

    
}
ll modInverse(ll n){
    ll p=mod;
    return power(n,p-2);
}
ll nCrModFermat(int n,int r){
    ll p=mod;
    if(r==0){
        return 1;
    }
    ll fac[n+1];
    fac[0]=1;
    for(int i=1;i<=n;i++){
        fac[i]=(fac[i-1]*i)%p;
    }
    return(fac[n]*modInverse(fac[r])%p*modInverse(fac[n-r])%p);

}
ll dfs(vector<int> g[],int s,int vis[],ll &a){
    a++;
    vis[s]=1;
    for(int i=0;i<g[s].size();i++){
        if(vis[g[s][i]]==1)continue;
        dfs(g,g[s][i],vis,a);
    }
    return a;
    

}


